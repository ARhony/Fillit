#include "../include/libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list *lst;
	t_list *tmp;

	lst = *alst;
	while (lst)
	{
		tmp = lst->next;
		del(lst, lst->content_size);
		lst = tmp;
	}
	*alst = NULL;
}
